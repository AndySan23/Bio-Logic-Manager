    public Inventario_Plantas() {
        initComponents();
        this.setLocationRelativeTo(null);
        
        javax.swing.table.DefaultTableModel modelo = new javax.swing.table.DefaultTableModel();
        tabla_inventario.setModel(modelo);
        
        modelo.addColumn("ID");
        modelo.addColumn("Especie");
        modelo.addColumn("Núm. Serie");
        modelo.addColumn("Estatus");
        modelo.addColumn("Fecha Siembra");
        
        try {
            java.sql.Connection cn = clases.Conexion.conectar();
            
            java.sql.PreparedStatement pst = cn.prepareStatement(
                    "select id_planta, especie, num_serie, estatus, fecha_siembra from plantas where estatus != 'Archivada'"
            );
            java.sql.ResultSet rs = pst.executeQuery();
            
            while (rs.next()) {
                Object [] fila = new Object [5];
                fila[0] = rs.getInt("id_planta");
                fila[1] = rs.getString("especie");
                fila[2] = rs.getString("num_serie");
                fila[3] = rs.getString("estatus");
                fila[4] = rs.getString("fecha_siembra");
                modelo.addRow(fila);
            }
            cn.close();
            
        } catch (Exception e) {
            System.err.println("Error al llenar tabla de plantas: " + e);
            javax.swing.JOptionPane.showMessageDialog(null, "Error al cargar inventario. Contacte al desarrollador.");
        }
    }

