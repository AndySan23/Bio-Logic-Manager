    private void tabla_inventarioMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabla_inventarioMouseClicked
        // TODO add your handling code here:
        int fila_clic = tabla_inventario.rowAtPoint(evt.getPoint());
        
        if (fila_clic > -1) {
            id_planta_update = Integer.parseInt(tabla_inventario.getValueAt(fila_clic, 0).toString());
            
            Informacion_Planta ventanaInfo = new Informacion_Planta();
            ventanaInfo.setVisible(true);
        }
    }//GEN-LAST:event_tabla_inventarioMouseClicked

    private void txt_buscadorKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txt_buscadorKeyReleased
        // TODO add your handling code here:
        String textoBuscado =  txt_buscador.getText();
        
        javax.swing.table.TableRowSorter<javax.swing.table.TableModel> trs = new javax.swing.table.TableRowSorter<>(tabla_inventario.getModel());
        
        tabla_inventario.setRowSorter(trs);
        
        if (textoBuscado.trim().length() == 0) {
            trs.setRowFilter(null);
        } else {
            trs.setRowFilter(javax.swing.RowFilter.regexFilter("(?i)" + textoBuscado));
        }
    }//GEN-LAST:event_txt_buscadorKeyReleased

